package com.dzteamdev.flexyp.Model;

public class Type {
    private String name,img;

    public Type() {
    }

    public Type(String name, String img) {
        this.name = name;
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
